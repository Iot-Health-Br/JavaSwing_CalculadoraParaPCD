    private void Bt_Op_PorcentagemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_Op_PorcentagemActionPerformed
        Numero_02 = Double.parseDouble(Txt_Lcd.getText());             
        //#Copular o Objeto        
        if(Operaçao=="Somar"){
            kelps.setNumero_02(Numero_01+Numero_02*(Numero_01/100));
            Operaçao = "Porcento";
        }
        if(Operaçao=="Subtrair"){
            kelps.setNumero_02(Numero_01-Numero_02*(Numero_01/100));
            Operaçao = "Porcento";
        }
        if(Operaçao=="Multiplicar"){
            kelps.setNumero_02(Numero_02*(Numero_01/100)); 
            Operaçao = "Porcento";
        }    
    }//GEN-LAST:event_Bt_Op_PorcentagemActionPerformed

