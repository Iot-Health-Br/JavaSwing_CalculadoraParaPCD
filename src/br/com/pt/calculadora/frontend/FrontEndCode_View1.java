    private void Bt_Op_IgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_Op_IgualActionPerformed
        
           switch(Operaçao){
               
            case "Porcento":
                String saida = "";
                saida += kelps.getNumero_02();
                Txt_Lcd.setText(saida);
                EnviaInfo();
                break;
               
            case "Somar":
                Numero_02 = Double.parseDouble(Txt_Lcd.getText());            
                //#Copular o Objeto
                kelps.setNumero_02(Numero_02);
                //#Publicando no txt Area
                saida = "";                                                                            
                saida += kelps.OpSomar() ;       
                Txt_Lcd.setText(saida);
                EnviaInfo();
                break;
                
            case "Subtrair":    
                Numero_02 = Double.parseDouble(Txt_Lcd.getText());            
                //#Copular o Objeto
                kelps.setNumero_02(Numero_02);
                //#Publicando no txt Area
                saida = "";                                                                            
                saida += kelps.OpSuntrair() ;       
                Txt_Lcd.setText(saida); 
                EnviaInfo();
                break;
            
            case "Multiplicar":    
                Numero_02 = Double.parseDouble(Txt_Lcd.getText());             
                //#Copular o Objeto
                kelps.setNumero_02(Numero_02);
                //#Publicando no txt Area
                saida = "";                                                                            
                saida += kelps.OpMultiplicar() ;       
                Txt_Lcd.setText(saida);
                EnviaInfo();
                break;  
                
            case "Dividir":
                try{
                    Numero_02 = Double.parseDouble(Txt_Lcd.getText());             
                    //#Copular o Objeto
                    kelps.setNumero_02(Numero_02);
                    //#Publicando no txt Area
                    saida = "";                                                                            
                    saida += kelps.OpDividir(Numero_01);       
                    Txt_Lcd.setText(saida);
                    EnviaInfo();
                }
                catch(Exception erro){
                    JOptionPane.showMessageDialog(this, erro.getMessage());
                }    
                break;  
                                               
            case "Raiz":
                try{
                    Numero_01 = Double.parseDouble(Txt_Lcd.getText());             
                    //#Copular o Objeto
                    kelps.setNumero_01(Numero_01);
                    //#Publicando no txt Area
                    saida = "";                                                                            
                    saida += kelps.OpRaizQuadrada(Numero_01);       
                    Txt_Lcd.setText(saida);
                    EnviaInfo();
                }
                catch(Exception erro){
                    JOptionPane.showMessageDialog(this, erro.getMessage());
                } 
                break;
            
            case "Potencializar":
                Numero_02 = Double.parseDouble(Txt_Lcd.getText());             
                //#Copular o Objeto
                kelps.setNumero_02(Numero_02);
                //#Publicando no txt Area
                saida = "";
                saida += kelps.OpPontecializar();
                Txt_Lcd.setText(saida);
                EnviaInfo();
                break;    

            case "Radiar":
                try{               
                    Numero_02 = Double.parseDouble(Txt_Lcd.getText());             
                    //#Copular o Objeto
                    kelps.setNumero_02(Numero_02);
                    //#Publicando no txt Area
                    saida = "";
                    saida += kelps.OpRadiar(Numero_01);
                    Txt_Lcd.setText(saida);
                    EnviaInfo();
                    break;
                }                
                catch(Exception erro){
                    JOptionPane.showMessageDialog(this, erro.getMessage());
                } 
               
        }                              
    }//GEN-LAST:event_Bt_Op_IgualActionPerformed

